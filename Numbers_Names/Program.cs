            {
                Console.WriteLine("two");
            }
            else if (num2 == 3)
            {
                Console.WriteLine("three");
            }
            else if (num2 == 4)
            {
                Console.WriteLine("four");
            }
            else if (num2 == 5)
            {
                Console.WriteLine("five");
            }
            else if (num2 == 6)
            {
                Console.WriteLine("six");
            }
            else if (num2 == 7)
            {
                Console.WriteLine("seven");
            }
            else if (num2 == 8)
            {
                Console.WriteLine("eight");
            }
            else if (num2 == 9)
            {
                Console.WriteLine("nine");
            }
            else if (num2 == 10)
